from flask import (
    Blueprint,
    Response,
    send_from_directory
)
from flask_swagger_ui import get_swaggerui_blueprint
from .manager import bp_manager
from .enterprise import bp_enterprise

bp_swagger: Blueprint = get_swaggerui_blueprint(
    base_url = '',
    api_url = '/swagger.yaml',
    blueprint_name = 'bp_swagger'
)

@bp_swagger.get('/swagger.yaml')
def swagger_yaml() -> tuple[Response, int]:
    return send_from_directory('/src', 'swagger.yaml'), 200

bps: list[Blueprint] = [
    bp_swagger,
    bp_manager,
    bp_enterprise
]
